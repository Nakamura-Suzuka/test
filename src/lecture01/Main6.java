package lecture01;

public class Main6 {
    public static void main (String[] args){
        //配列

        //Javaにおいて配列を定義するには以下のように記述する
        //型名[]　変数名　= new 型名[要素数];

        //例
        int[] array = new int[10]; //int型の配列を10要素分確保

        //アクセスするには以下のように記述する
        array[4] = 3; //arrayの5番目に3を代入する
    }
}
